package chapter5_3_function;

public class FunctionTest {

	public static int addNum(int num1, int num2) { // return 값이 정수형(integer)이므로, int로 선언
		int result;
		result = num1+num2;
		return result;
	}
	
	public static void sayHello(String greeting) { // return 값이 없을 때 => void
		System.out.println(greeting);
	}
	
	
	public static int calcSum() {
		int sum=0;
		int i;
		
		for (i = 0; i <= 100; i++) {
			sum+=i;
		}
		
		return sum;
	}
	
	
	public static void main(String[] args) {
		int n1 = 10;
		int n2 = 20;
	
		int total = addNum(n1, n2);
		System.out.println(total);
		
		sayHello("HELLO");
		
		int calResult=calcSum();
		System.out.println(calResult);
	}

}
